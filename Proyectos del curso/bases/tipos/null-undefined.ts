
//para que sirve el tipo undefined y null?
// El tipo undefined se utiliza para indicar que una variable no ha sido asignada a ningún valor
// o que una función no devuelve ningún valor. Por otro lado, el tipo null se utiliza para indicar que una variable tiene un valor nulo o vacío.
// En TypeScript, ambos tipos son diferentes y se utilizan en diferentes contextos.
// En este caso, se muestra cómo declarar variables de tipo undefined y null, y cómo asignarles valores.
let nada: undefined =  undefined; // variable de tipo undefined
console.log(nada);

let noDefinido: null = null; // variable de tipo null